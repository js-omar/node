/**
 * Sum is a function that takes two numbers and returns the sum of those two numbers
 * @param {number} num1 - number - This is the first parameter, and it's a number.
 * @param {number} num2 - number - This is the type of the parameter.
 */
export const sum = (num1: number, num2: number): number => num1 + num2;

/**
 * Minus is a function that takes two numbers as arguments and returns the difference of the two
 * numbers.
 * @param {number} num1 - number - This is the first parameter, and it's a number.
 * @param {number} num2 - number - This is the second parameter, and it's a number.
 */
export const minus = (num1: number, num2: number): number => num1 - num2;
